package Variables;

public class StringConcatenation {

	public static void main(String[] args) {
		String name = "Fatih";
		String lastName = "Can";
		char grade = 'A';
		String city = "VirginiaBeach";
		String state = "VA";
		String phoneNumber = "7577732030";
		
		System.out.println(name);
		System.out.println(lastName);
		System.out.println(grade);
		System.out.println(city);
		System.out.println(state);
		System.out.println(phoneNumber);
		
		System.out.println("My name is " + name + " and my last name is " + lastName + ". I am a " + grade + " student and I live in " + city + " " + state + ". My phone number is " + phoneNumber + ".");
		

	}

}

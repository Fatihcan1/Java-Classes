package Class7;

public class CompoundOperators {

	public static void main(String[] args) {
		int a=100;
		a+=100;
		
		System.out.println(a);
		
		int b=200;
		
		b-=67;
		
		
		System.out.println(b);

		
		double cakePiece=11;
		
		cakePiece/=4;
		
		System.out.println(cakePiece);
		
		cakePiece=25;
		cakePiece%=7;
		
		System.out.println(cakePiece);
		
		
		
		
		
		
	}

}
